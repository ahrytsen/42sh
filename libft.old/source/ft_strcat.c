char	*ft_strcat(char *s1, const char *s2)
{
	char *str;

	if (s1 && s2)
	{
		str = s1;
		while (*str)
			str++;
		while (*s2)
			*str++ = *s2++;
		*str = '\0';
	}
	return (s1);
}
