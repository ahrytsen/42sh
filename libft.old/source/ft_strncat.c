#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *str;

	if (s1 && s2)
	{
		str = s1;
		while (*str)
			str++;
		while (*s2 && n)
		{
			*str++ = *s2++;
			n--;
		}
		*str = '\0';
	}
	return (s1);
}
