#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	if (n < 10 && n > -10)
	{
		if (n < 0)
		{
			write(1, "-", 1);
			c = n * -1 + 48;
		}
		else
			c = n + 48;
		write(1, &c, 1);
	}
	else
	{
		if (n > 9)
			c = n % 10 + 48;
		else
			c = (n % 10) * -1 + 48;
		ft_putnbr(n / 10);
		write(1, &c, 1);
	}
}
