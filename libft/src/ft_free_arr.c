#include "libft.h"

void	ft_free_arr(void **arr)
{
	void **ptr;

	if (arr)
	{
		ptr = arr;
		while (*ptr)
		{
			free(*ptr);
			ptr++;
		}
		free(arr);
		arr = NULL;
	}
}
