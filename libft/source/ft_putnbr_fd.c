#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	if (n < 10 && n > -10)
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			c = n * -1 + 48;
		}
		else
			c = n + 48;
		write(fd, &c, 1);
	}
	else
	{
		if (n > 9)
			c = n % 10 + 48;
		else
			c = (n % 10) * -1 + 48;
		ft_putnbr_fd(n / 10, fd);
		write(fd, &c, 1);
	}
}
