#include "ft_sh.h"

char	*get_last_var(char *s, char *buf, int *i)
{
	if (s)
		while (*s)
		{
			if (*s == '\\' && *++s)
				*s != '\n' ? buf[(*i)++] = *++s : (int)s++;
			else if (*s)
				buf[(*i)++] = *s++;
		}
	return (buf);
}
